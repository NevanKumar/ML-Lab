import numpy as np

array1 = np.array([10, 20, 30, 40])
array2 = np.array([1, 2, 3, 4])
print("Array1:",array1)
print("Array2:",array2)

array_add = array1 + array2
print("Addition of arrays:", array_add)

array_sub = array1 - array2
print("Subtraction of arrays:", array_sub)

array_mul = array1 * array2
print("Multiplication of arrays:", array_mul)

array_div = array1 / array2
print("Division of arrays:", array_div)